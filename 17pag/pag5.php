    <div class="quad justify">
        <p>HTTPS é a sigla para "Hypertext Transfer Protocol Secure". É uma versão segura do HTTP, que é o protocolo
            usado para transferir dados na web. A principal diferença entre HTTP e HTTPS é que o HTTPS utiliza
            criptografia para proteger a comunicação entre o navegador e o servidor web.</p>

        <h3>Pontos principais sobre HTTPS:</h3>
        <ol>
            <li><strong>Criptografia</strong>: HTTPS usa um protocolo de criptografia, como o TLS (Transport Layer
                Security) ou o SSL (Secure Sockets Layer), para garantir que os dados transmitidos entre o navegador e o
                servidor estejam protegidos contra interceptações e ataques.</li>

            <li><strong>Autenticação</strong>: HTTPS verifica a identidade do site que você está visitando, ajudando a
                garantir que você está realmente conectado ao site legítimo e não a um site falso criado para enganar os
                usuários.</li>

            <li><strong>Integridade dos Dados</strong>: A criptografia também ajuda a garantir que os dados não sejam
                alterados durante a transmissão.</li>

            <li><strong>Certificado SSL/TLS</strong>: Para que um site possa usar HTTPS, ele deve ter um certificado
                SSL/TLS válido, que é emitido por uma autoridade certificadora. Este certificado é usado para
                estabelecer uma conexão segura entre o servidor e o navegador.</li>
        </ol>

        <h2>Conclusão</h2>
        <p>Em resumo, o HTTPS é fundamental para garantir a segurança e a privacidade das informações trocadas na
            internet, especialmente em transações financeiras, logins e outras atividades que envolvem dados sensíveis.
        </p>
