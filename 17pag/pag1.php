            <p style="margin-right: 60px;">O Que é a WWW?
                A World Wide Web, comumente conhecida como WWW ou simplesmente web, é um sistema de
                documentos interconectados e acessíveis pela internet. Criada por Tim Berners-Lee em 1989 e
                lançada ao público em 1991, a WWW revolucionou a forma como acessamos e compartilhamos
                informações.

            <p>Como Funciona a WWW?
                A WWW é composta por páginas web que são armazenadas em servidores e acessadas através de
                navegadores. Aqui está uma visão geral de como tudo funciona:</p>

            URLs (Uniform Resource Locators): Cada página web tem um endereço único, chamado URL. Por
            exemplo, https://www.exemplo.com.</p>

            <p>Navegadores: Programas como Google Chrome, Mozilla Firefox e Safari são usados para acessar
                páginas web. Eles interpretam o código das páginas e exibem o conteúdo para o usuário.
            <p>Protocolos: A WWW utiliza protocolos como HTTP (Hypertext Transfer Protocol) e HTTPS (HTTP
                Secure) para garantir que as informações sejam transmitidas corretamente e com segurança
                entre o navegador e o servidor.</p>

            <p>Servidores: As páginas web são armazenadas em servidores, que são computadores
                especializados em armazenar e enviar páginas para navegadores. Quando você acessa um site,
                seu navegador envia uma solicitação para o servidor, que responde com os dados da página
                solicitada.

            <p>HTML (Hypertext Markup Language): A principal linguagem usada para criar páginas web. HTML
                estrutura o conteúdo das páginas e define como o texto, imagens e outros elementos devem ser
                exibidos.

            <p>CSS (Cascading Style Sheets): Utilizado para estilizar as páginas web. O CSS controla o
                layout, as cores e as fontes, proporcionando um visual agradável e consistente.

            <p>JavaScript: Uma linguagem de programação usada para adicionar interatividade às páginas web.
                Com JavaScript, é possível criar efeitos dinâmicos, como animações e formulários
                interativos.

            <p>A Evolução da WWW
                Desde seu lançamento, a WWW evoluiu significativamente. No início, era uma rede de
                documentos simples e estáticos. Hoje, a web é interativa e dinâmica, com sites que oferecem
                desde e-commerce e redes sociais até aplicações web complexas e streaming de vídeo.

            <p>Por Que a WWW é Importante?
                A WWW transformou a forma como trabalhamos, nos comunicamos e consumimos informação. Ela
                possibilitou:

            <p>Acesso Imediato: Informações de qualquer lugar do mundo estão a poucos cliques de distância.
                Comunicação Global: Facilita a comunicação através de e-mails, redes sociais e mensagens
                instantâneas.
            <p>Inovação: Permite a criação e distribuição de novas tecnologias e serviços, desde startups
                até grandes corporações.
            <p>Conclusão</p>
            <p>A World Wide Web é a espinha dorsal da internet moderna, permitindo a comunicação, o
                compartilhamento de informações e a criação de novas experiências digitais. Esperamos que
                esta introdução tenha ajudado a esclarecer o que é a WWW e como ela faz parte do seu
                cotidiano online.</p>

